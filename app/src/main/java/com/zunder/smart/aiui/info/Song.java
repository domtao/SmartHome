package com.zunder.smart.aiui.info;

public class Song {

	public int Id;
	public String SongAuthor;
	public String SongName;
	public String SongType;
	public String SongUrl;
	public String SongRemark;
	/**
	 * @return the id
	 */
	public int getId() {
		return Id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		Id = id;
	}
	/**
	 * @return the songAuthor
	 */
	public String getSongAuthor() {
		return SongAuthor;
	}
	/**
	 * @param songAuthor the songAuthor to set
	 */
	public void setSongAuthor(String songAuthor) {
		SongAuthor = songAuthor;
	}
	/**
	 * @return the songName
	 */
	public String getSongName() {
		return SongName;
	}
	/**
	 * @param songName the songName to set
	 */
	public void setSongName(String songName) {
		SongName = songName;
	}
	/**
	 * @return the songType
	 */
	public String getSongType() {
		return SongType;
	}
	/**
	 * @param songType the songType to set
	 */
	public void setSongType(String songType) {
		SongType = songType;
	}
	/**
	 * @return the songUrl
	 */
	public String getSongUrl() {
		return SongUrl;
	}
	/**
	 * @param songUrl the songUrl to set
	 */
	public void setSongUrl(String songUrl) {
		SongUrl = songUrl;
	}
	/**
	 * @return the songRemark
	 */
	public String getSongRemark() {
		return SongRemark;
	}
	/**
	 * @param songRemark the songRemark to set
	 */
	public void setSongRemark(String songRemark) {
		SongRemark = songRemark;
	}
}
