package com.zunder.smart.model;

public class FunctionParam {
	private int Id;
	private String Function_param;
	private int TypeId;
	private String Function_paramLanguage;

	/**
	 * @return the id
	 */
	public int getId() {
		return Id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(int id) {
		Id = id;
	}

	/**
	 * @return the function_param
	 */
	public String getFunction_param() {
		return Function_param;
	}

	/**
	 * @param function_param
	 *            the function_param to set
	 */
	public void setFunction_param(String function_param) {
		Function_param = function_param;
	}

	/**
	 * @return the typeId
	 */
	public int getTypeId() {
		return TypeId;
	}

	/**
	 * @param typeId
	 *            the typeId to set
	 */
	public void setTypeId(int typeId) {
		TypeId = typeId;
	}

	/**
	 * @return the function_paramLanguage
	 */
	public String getFunction_paramLanguage() {
		return Function_paramLanguage;
	}

	/**
	 * @param function_paramLanguage
	 *            the function_paramLanguage to set
	 */
	public void setFunction_paramLanguage(String function_paramLanguage) {
		Function_paramLanguage = function_paramLanguage;
	}

}
