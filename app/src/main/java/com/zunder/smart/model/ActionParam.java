package com.zunder.smart.model;

public class ActionParam {
	private int Id;
	private String Actionparam;
	private int TypeId;
	private String Action_paramLanguage;
	/**
	 * @return the id
	 */
	public int getId() {
		return Id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		Id = id;
	}
	/**
	 * @return the actionparam
	 */
	public String getActionparam() {
		return Actionparam;
	}
	/**
	 * @param actionparam the actionparam to set
	 */
	public void setActionparam(String actionparam) {
		Actionparam = actionparam;
	}
	/**
	 * @return the typeId
	 */
	public int getTypeId() {
		return TypeId;
	}
	/**
	 * @param typeId the typeId to set
	 */
	public void setTypeId(int typeId) {
		TypeId = typeId;
	}
	/**
	 * @return the action_paramLanguage
	 */
	public String getAction_paramLanguage() {
		return Action_paramLanguage;
	}
	/**
	 * @param action_paramLanguage the action_paramLanguage to set
	 */
	public void setAction_paramLanguage(String action_paramLanguage) {
		Action_paramLanguage = action_paramLanguage;
	}
}
