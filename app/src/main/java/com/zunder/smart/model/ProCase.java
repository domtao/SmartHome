package com.zunder.smart.model;

import android.graphics.Bitmap;

public class ProCase {
	private String ProCaseName;
	private String ProCaseAlia;
	private String ProCaseImage;
	private String ProCaseKey;
	private int ProCaseIndex;
	public String getProCaseName() {
		return ProCaseName;
	}

	public void setProCaseName(String proCaseName) {
		ProCaseName = proCaseName;
	}

	public String getProCaseImage() {
		return ProCaseImage;
	}

	public void setProCaseImage(String proCaseImage) {
		ProCaseImage = proCaseImage;
	}

	public String getProCaseKey() {
		return ProCaseKey;
	}

	public void setProCaseKey(String proCaseKey) {
		ProCaseKey = proCaseKey;
	}


	public String getProCaseAlia() {
		return ProCaseAlia;
	}

	public void setProCaseAlia(String proCaseAlia) {
		ProCaseAlia = proCaseAlia;
	}

	public int getProCaseIndex() {
		return ProCaseIndex;
	}

	public void setProCaseIndex(int proCaseIndex) {
		ProCaseIndex = proCaseIndex;
	}
}
